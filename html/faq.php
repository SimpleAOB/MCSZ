        <h1>Frequently Asked Questions</h1><hr>
        <b>What is Minecraft Server Zone?</b>
        <p>Minecraft Server Zone is an area for servers to connect with players trying to find there next best server. Players are able to promote their preferred servers by voting for them. By voting for your favorite server, you allow the server to be advertised to thousands of another players looking for that next server for them!</p>
        <b>How do I vote for a server?</b>
        <p>To vote for a server, you begin by locating the server and pressing the "Vote" button next to the server. Next, locate the area for your Minecraft IGN, enter the correct verification code, and congratulations, you have voted for a server!</p>
        <b>What is votifier?</b>
        <p>Votifier is a plugin that can be used on your server to alert players as to when a person has voted for your server. Votifier gives server owners the opportunity to reward players that vote. More information can be found on the <a href="http://dev.bukkit.org/bukkit-plugins/votifier/" target="_blank">official plugin page</a>.</p>
        <b>How do I add a server?</b>
        <p>To add a server, you simply locate the gear in the top right of the site, select it, and click add resources. From there, you will be sent to the server adding page where you can add create your server for MCSZ</p>
        <b>Where did some of my votes go?</b>
        <p>Votes are active for a month and once a month has passed, that vote is deleted. This is to give all servers and equal opportunity to grow and also allows for new servers to compete with the best. Votes may also be removed if we suspect any sort
            of "buying of votes" or cheating when voting for a server.</p>
        <b>Why isn't my server being shown?</b>
        <p>Servers only show up once a single vote has been placed on the server. This can be anyone's vote, even the server owners but, for a server to show in our searches and random servers section, it needs one vote.</p>
        <b>Can I change my password?</b>
        <p>Yes, to change your password, click on the settings button in the top right. Go to account settings and enter your current password, new password, and confirmation.</p>
        <b>Why isn't my server banner working?</b>
        <p>Server banners must be 468x60 pixels and if not, the banner will not be accepted.</p>
        <b>Why has my server been deleted?</b>
        <p>Servers are usually deleted due to a breach in the terms and conditions. When a server is deleted, it is considered a warning for a server owner and if caught again, the IP used to create the account will be banned. If there is questions after
            you have reread the terms and conditions regarding your server, contact support@mcsz.net.</p>
        <b>Why has my banner been removed from my server?</b>
        <p>Banners are usually removed due to a breach in the terms and conditions for MCSZ or can be removed upon the judgment of staff.</p>
        <b>What are sponsored servers?</b>
        <p>A sponsored server is a server that has won one of the 5 slots. The 5 sponsored slots are auctioned of bi-weekly near the end of every sponsor term. For more information, head to the Sponsor Section.</p>
        <b>My question wasn't answered.</b>
        <p>If your question has still not been answered by any of the following questions above, please email support@mscz.net. You should receive a response within the next 2 business days.</p>
